#include "CBEntityComponentSystem/Core/Entity.h"

#include <boost/uuid/random_generator.hpp>

//-------------------------------------------------------------------
// CONSTRUCTOR - DESTRUCTOR
//-------------------------------------------------------------------

cb::ecs::Entity::Entity()
	: m_is_null(true)
	, m_id(boost::uuids::random_generator()())
{
}

//-------------------------------------------------------------------
// PUBLIC
//-------------------------------------------------------------------

bool cb::ecs::Entity::isNull()
{
	return m_is_null;
}

//-------------------------------------------------------------------

void cb::ecs::Entity::setIsNull(bool isNull)
{
	m_is_null = isNull;
}

//-------------------------------------------------------------------

boost::uuids::uuid cb::ecs::Entity::guid() const
{
	return m_id;
}

//-------------------------------------------------------------------
// PROTECTED
//-------------------------------------------------------------------

//-------------------------------------------------------------------
// PRIVATE
//-------------------------------------------------------------------


//----------------------------------------------------------------------------------------

bool cb::ecs::operator==(const cb::ecs::Entity& lhs, const cb::ecs::Entity& rhs)
{
	return lhs.guid() == rhs.guid();
}

//----------------------------------------------------------------------------------------

bool cb::ecs::operator!=(const cb::ecs::Entity& lhs, const cb::ecs::Entity& rhs)
{
	return !(lhs == rhs);
}

